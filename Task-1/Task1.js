var x = 0; //initiating the value of x
var y = 1; //initial value of y
console.log(x); // Prints the value of x which is 0
console.log(y); // Prints the value of y which is 1

var x = x + 3; // adds x=0 and 3 to the new x-value
var y = y + x; // adds y=1 to the updated x=3
console.log(x); // Prints the new x value which is 3
console.log(y); //prints the new y value which is 4